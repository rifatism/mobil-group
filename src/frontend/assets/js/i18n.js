  /* ── Ядро i18n ────────────────────────────────────────────────────────── */
  let _lang = localStorage.getItem('site_lang') || 'ru';

  function t(key) {
    return (T[_lang] && T[_lang][key]) || (T.ru && T.ru[key]) || key;
  }

  function applyLang(lang) {
    _lang = lang;
    localStorage.setItem('site_lang', lang);
    document.documentElement.lang = lang;

    const titleKey = document.documentElement.getAttribute('data-i18n-title');
    if (titleKey) document.title = t(titleKey);

    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = t(el.getAttribute('data-i18n'));
    });

    // Сохраняем оригинальный HTML один раз, восстанавливаем при RU
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      if (!el.dataset.origHtml) el.dataset.origHtml = el.innerHTML;
      el.innerHTML = lang === 'ru'
        ? el.dataset.origHtml
        : (t(el.getAttribute('data-i18n-html')) || el.dataset.origHtml);
    });

    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      if (!el.dataset.origPh) el.dataset.origPh = el.placeholder;
      el.placeholder = lang === 'ru'
        ? el.dataset.origPh
        : (t(el.getAttribute('data-i18n-ph')) || el.dataset.origPh);
    });

    document.querySelectorAll('.lang-label').forEach(el => {
      el.textContent = lang.toUpperCase();
    });
  }

  function toggleLang() {
    applyLang(_lang === 'ru' ? 'en' : 'ru');
  }

  /* ── Публичный API ────────────────────────────────────────────────────── */
  window.i18n      = { t, applyLang, toggleLang, getLang: () => _lang };
